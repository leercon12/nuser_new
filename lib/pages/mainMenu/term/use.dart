1. 회사는 본 약관이 금지하거나 관련 법령 기타 공서양속에 반하는 행위를 하지 않으며 본 약관이 정하는 바에 따라 지속적이고, 안정적으로 서비스를 제공하는 데 최선을 다한다.  
