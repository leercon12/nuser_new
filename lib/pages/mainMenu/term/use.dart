**제 12조 (포인트 이용)**

1. 포인트는 무형 재화로 포인트에 대한 뭐든 권리는 포인트의 소유주에게 있으며, 포인트 이용 대상인 회사가 아닐 경우, 회사는 포인트 거래에 대한 책임을 지지 않는다.  
1. 전자세금계산서 발행 수수료는 발행시 차감되며, 국세청 전송이 실패해도 환불되지 않는다. 단, 발행 실패일 경우에는 전액 환불한다.  
1. 이용권의 단순 환불은 불가하며, 구매시 30일동안 이용후 구매 해제된다. 이후 특별한 상황에 대한 환불은 환불 규칙을 따른다.  
1. 회원이 시스템이 제공하지 않는 방법으로 포인트를 이용하는 것에 대해 회사는 책임을 지지 않는다.  
1. 회원은 본인이 소유한 포인트를 타인에게 양도, 대여 또는 담보의 목적으로 이용할 수 없다.  
1. 회원은 포인트를 부정하게 이용하여서는 아니 되며, 회사는 포인트, 쿠폰에 대한 적법한 권리가 있는 회원으로부터 부정이용 신고 등을 접수한 경우에는 즉시 이를 해결하기 위한 조치를 취하기로 한다.  
1. 회원은 적립된 포인트를 출금외 대체 방식으로 반환 요청할 수 없다.  
1. 포인트 충전 기준은뢰사의 방침에 따라 임의로 변경될 수 있다.   
1. 회원정보의 변경으로 인한 포인트 이전 등의 처리는 회사의 내부 방침에 따른다.

**제 13조 (포인트 등 충전)**

1. 회원은 회사가 제공하는 방법으로 포인트를 충전한다.  
1. 다음 각호에 해당하는 경우 회사 또는 파트너는 회원의 충전을 제한할 수 있다.  
   * 회사 충전 정책에 반하는 경우  
   * 비정상적인 방법으로 서비스를 이용하는 것으로 판단되는 경우  
   * 타인의 명의, 계좌정보 등을 도용하여 충전하는 것으로 판단되는 경우  
   * 기타 충전이 불가능한 기술적, 제도적 사유가 발생한 경우  
1. 회원은 본 조의 충전에 관한 사항은 파트너의 각 정책에 따라 운영되고 있으며, 기사 회원과 주선사 회원 사이에 포인트에 관한 분쟁이 발생할 경우에도 회사는 해당 분쟁을 해결하거나 중재할 의무가 없으며, 이에 관하여 아무런 법적 책임을 부담하지 않는 것임을 이해하였으며, 이에 동의한다

**제 14조 (포인트 등 정정, 취소)**

1. 적립된 포인트에 오류가 있을 경우 회원은 오류발생 시점부터 7일 이내 회사나 파트너에 정정 신청을 하여야 하며, 회사나 파트너는 회원의 정정 신청일로부터 최대 30일 이내에 조치를 취하기로 한다. 단, 회원은 필요한 경우 포인트, 쿠폰 적립 오류를 증명할 수 있는 객관적 자료(영수증 등)를 제시하여야 한다.  
1. 회원이 상품 등의 구매취소, 반품, 기타의 사유에 의해 포인트, 쿠폰의 적립이 취소되는 경우 취소분만큼 기 지급된 포인트, 쿠폰은 자동으로 차감된다.  
1. 제휴사 포인트, 쿠폰에 관해서는 제휴사가 정하거나 제휴사의 자체적인 제휴사 정책에 따르며, 제휴사의 제휴사 정책에 대해서는 제휴사가 책임을 지고 회사는 회원에 대하여 어떠한 책임을 지지 않는다.

**제 15조 (포인트 유효기간 및 소멸)**

1. 보유한 모든 포인트의 출금없이 탈퇴할 수 없으며, 회원이 계정을 이용하지 않아도 포인트는 영구히 보관된다. 만약 회사가 포인트를 반환해야 할 경우, 등록된 계좌로 반환되며, 해당 증명을 발송한다.  
1. 포인트는 유효기간 없이 영구히 이용가능하다.  
1. 회사는 포인트 관련 모든 내역을 앱에서 볼 수 있도록 조치한다.  
 


**제 16 조 (모바일 앱 이용)**

1. 회원은 모바일 앱을 이용하여 포인트 상태를 관리하거나, 회원의 현재 위치 주변에 화물 운송건을 검색할 수 있다..  
1. 회원이 모바일 앱을 이용하고자 하는 경우, 회원은 회사가 회원의 위치정보를 이용한 서비스 제공을 목적으로 하는 도도포인트위치정보 이용약관에 동의하고, 회사는 개인의 위치정보를 수집하지 않는다.

**제 18 조 (콘텐츠)**

1. 회사의 서비스를 통하여 제공되는 모든 정보 및 게시물 등(이하 콘텐츠라 함)의 지식재산권은 회사에 있으며, 회원은 본 약관의 내용에 따라 서비스를 이용하는 것 외에는 지식재산권에 대한 어떠한 권리와 이익도 가지지 아니한다.  
1. 회원은 회사의 콘텐츠를 서비스 제공 목적 외로 사용할 수 없고, 회원은 직접 또는 특수관계인을 이용하는 등 간접적인 방법으로 서비스에 관련된 지적재산권을 침해하거나 회사의 지적재산권 행사를 방해해서는 아니된다. 상기 침해 행위에는 서비스에 관한 복제, 역설계, 해독, 역편집, 해체, 분석, 소스코드 도출, 개조, 변환, 변환, 파생 작업, 조정, 다른 소프트웨어와의 병합, 결함 수정, 보안 기능의 우회, 등록 시도, 타인에게 서비스를 사용하게 하는 등 본 약관의 내용을 넘어서는 서비스의 사용이 포함된다.  
1. 회원은 서비스 내 등록하는 게시물이 제3자의 상표권, 특허권, 저작권, 성명권, 초상권 등 제반 지식재산권, 인격권을 침해하지 않으며, 회사 정책 가이드를 준수하고, 제3자의 초상권, 지식재산권을 사용한 경우 정당한 권리자로부터 사용 허락을 받았음을 보증한다. 회사는 이러한 보증에 부합하지 아니하는 회원의 게시물을 수정하거나 삭제할 수 있고, 회원의 게시물에 관하여 제3자가 지식재산권과 관련한 권리침해 이의제기 또는 소송이 발생할 경우 회사는 해당 게시물을 차단하거나 삭제할 수 있으며, 이 경우 회원은 회사를 면책시켜야 하며 그로 인해 발생하는 회사와 제3자의 모든 손해를 배상하여야 한다.  
1. 회사는 게시물을 무상으로 이용할 권리를 가진다(배포권, 복제권, 공연권, 공중송신권, 전시권 및 번역 · 편곡 · 변형 · 각색 · 영상제작 그 밖의 방법 작성하는 창작물에 대한 이용 권리 포함).  
1. 본 조는 서비스 이용 계약이 종료된 이후에도 효력을 가진다.

