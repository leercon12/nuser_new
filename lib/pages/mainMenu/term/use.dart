   * 회원가입 신청 또는 회원정보 변경 시 허위 내용을 등록하는 행위  
   * 타인의 정보를 도용하는 행위  
   * 회사가 게시한 정보를 무단 변경, 삭제, 전재, 공유, 배포하는 행위  
   * 다른 회원의 개인정보 및 계정정보를 수집하는 행위  
   * 회사의 사전동의 없이 영리 목적으로 서비스를 이용하거나, 광고성 정보를 전송하기 위하여 서비스를 이용하는 행위  
   * 리버스엔지니어링, 디컴파일, 디스어셈블 및 기타 일체의 가공행위를 통한 서비스의 복제, 분해, 모방 및 기타 변경 등을 하는 행위  
   * 자동접속 프로그램을 사용하는 등 비정상적인 방법으로 서비스를 이용하여 회사의 서버에 부하를 일으켜 정상적인 서비스의 제공 또는 이용을 방해하는 행위  
   * 본인이 아닌 제3자에게 접속 권한을 부여하는 행위  
   * 회사 또는 제3자의 지적재산권을 침해하는 행위  
   * 회사 또는 제3자의 명예를 훼손하거나 업무를 방해하는 행위  
   * 외설 또는 폭력적인 메시지, 화상, 음성, 기타 미풍양속에 반하는 정보를 서비스에 공개 또는 게시하는 행위  
   * 회사가 안내하는 서비스 이용방법에 의하지 아니하고 비정상적인 방법으로 서비스를 이용하거나 회사의 시스템에 접근하는 행위  
   * 다른 회원의 서비스 이용을 방해하는 행위  
   * 기타 서비스의 안정적인 운영에 지장을 주거나 줄 우려가 있는 일체의 행위  
1. 회원은 관계법령, 본 약관, 이용안내 및 서비스와 관련하여 회사가 통지하는 사항 등을 준수하여야 하며, 위 사항을 위반하거나 기타 회사의 업무에 방해되는 행위를 하여서는 아니 된다.  
1. 휴대전화번호에 관한 관리책임은 회원에게 있으며, 이를 소홀히 하여 발생한 모든 민∙형사상의 책임은 회원 자신에게 있다.  
1. 회원의 휴대전화번호 변경에 따라 기존에 수신 받은 혜택이 삭제된 경우 재발송이 불가하며, 휴대전화번호 변경 시에 혜택의 보관 및 이전의 책임은 회원에게 있다.

**제11조 (이용자의 정보보호)**

